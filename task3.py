"""
Задача 3. Минимальный путь в матрице
Дана таблица штрафов 4×4. Разрешено ходить только вправо или вниз.
Нужно найти путь с минимальной суммой штрафов от верхнего левого угла к правому нижнему.
Ответ: минимальная сумма.

ПРОГРАММА ДОЛЖНА ПЕЧАТАТЬ НА ЭКРАНЕ ТОЛЬКО ОДНО ЧИСЛО (ОТВЕТ) И НИЧЕГО БОЛЬШЕ.
"""

# Фиксированная матрица штрафов 4x4 для определенности ответа
GRID = [
    [10, 20, 30, 40],
    [5,  1,  2,  10],
    [4,  2,  1,  5 ],
    [1,  20, 30, 2 ]
]


dp = [[0 for _ in range(4)] for _ in range(4)]


dp[0][0] = GRID[0][0]


for j in range(1, 4):
    dp[0][j] = dp[0][j-1] + GRID[0][j]

for i in range(1, 4):
    dp[i][0] = dp[i-1][0] + GRID[i][0]

for i in range(1, 4):
    for j in range(1, 4):
        dp[i][j] = min(dp[i-1][j], dp[i][j-1]) + GRID[i][j]

print(dp[3][3])